import { NestFactory } from '@nestjs/core';
import { SwaggerModule, DocumentBuilder } from '@nestjs/swagger';
import { AppModule } from './app.module';
import { ValidationPipe } from '@nestjs/common';

async function bootstrap() {
  const app = await NestFactory.create(AppModule);

  app.useGlobalPipes(new ValidationPipe());
  app.enableCors();

  const config = new DocumentBuilder()
    .setTitle('Smart Contract Verifier API')
    .setDescription(
      'Smart Contract Verifier is a free, Open Source REST API for verifying Sophia Smart Contracts source code deployed in Ã¦ternity blockchain and sharing that code with the community. Contract source code submitted by the users will be verified and freely accessible by everyone.',
    )
    .setVersion('1.0.0')
    .build();
  const document = SwaggerModule.createDocument(app, config);
  SwaggerModule.setup('api', app, document);

  await app.listen(3000);
}
bootstrap();
